def is_chinese(char: str) -> bool:
    """判断字符是否为汉字（CJK 统一汉字区块）。"""
    cp = ord(char)
    return (
        0x4E00 <= cp <= 0x9FFF    # CJK Unified Ideographs (基本区)
        or 0x3400 <= cp <= 0x4DBF  # CJK Extension A
        or 0x20000 <= cp <= 0x2A6DF  # CJK Extension B
        or 0xF900 <= cp <= 0xFAFF   # CJK Compatibility Ideographs
        or 0x2F800 <= cp <= 0x2FA1F  # CJK Compatibility Supplement
    )


def get_stroke_count(char: str) -> int:
    """返回汉字笔画数。未知汉字返回 -1。"""
    return _EXACT.get(char, -1)


def analyze_text(text: str) -> list[tuple[str, int]]:
    """
    分析文本，返回 [(汉字, 笔画数), ...]。
    自动过滤英文、数字、标点及其它非汉字字符。
    笔画数 == -1 表示该汉字不在字典中（未知）。
    """
    result = []
    seen = {}  # 去重统计用（不影响输出顺序）
    for ch in text:
        if is_chinese(ch):
            result.append((ch, get_stroke_count(ch)))
    return result


def summary(items: list[tuple[str, int]]) -> dict:
    """
    统计摘要：
      total_chars  — 汉字总数（含重复）
      unique_chars — 不重复汉字数
      known_chars  — 在字典中的汉字数
      total_strokes — 总笔画数（仅统计已知汉字）
      unknown_list  — 未知笔画数的汉字列表（去重）
    """
    total_chars = len(items)
    unique_chars = len(set(ch for ch, _ in items))
    known = [(ch, s) for ch, s in items if s >= 0]
    unknown = list(dict.fromkeys(ch for ch, s in items if s < 0))
    total_strokes = sum(s for _, s in known)
    return {
        "total_chars":   total_chars,
        "unique_chars":  unique_chars,
        "known_chars":   len(known),
        "total_strokes": total_strokes,
        "unknown_list":  unknown,
    }
